-- master_exercise_examples: MasterExercise に紐付く入力/期待出力の例。MasterExerciseExample に対応。
CREATE TABLE IF NOT EXISTS master_exercise_examples (
    id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    exercise_id     BIGINT   NOT NULL,
    order_index     SMALLINT NOT NULL,
    input_text      TEXT     NOT NULL DEFAULT '',
    expected_output TEXT     NOT NULL,
    created_at      TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at      TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    -- 例は必ず実在する演習に紐付く。親削除時は例も道連れにし孤児データを防ぐ。
    CONSTRAINT fk_master_exercise_examples_exercise
        FOREIGN KEY (exercise_id) REFERENCES master_exercises (id) ON DELETE CASCADE
);

-- 同一問題内での順序衝突を DB レベルで弾く。
CREATE UNIQUE INDEX IF NOT EXISTS idx_examples_exercise_order
    ON master_exercise_examples (exercise_id, order_index);

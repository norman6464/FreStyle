-- invitations: 企業担当者を招待するマジックリンク。AdminInvitation エンティティに対応。
-- 型は H2(PostgreSQL モード) と PostgreSQL の双方で通る書き方。
CREATE TABLE IF NOT EXISTS invitations (
    id           BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    company_id   BIGINT,
    email        VARCHAR(255),
    -- role / status は認可分岐のキーなので NOT NULL。status は既定 pending。
    role         VARCHAR(32)  NOT NULL,
    display_name VARCHAR(255),
    status       VARCHAR(16)  NOT NULL DEFAULT 'pending',
    token        VARCHAR(64),
    expires_at   TIMESTAMP,
    created_at   TIMESTAMP
);

-- token はマジックリンク照合のキー。一意かつ高速参照のため UNIQUE 索引。
CREATE UNIQUE INDEX IF NOT EXISTS idx_invitations_token ON invitations (token);
-- email / company_id でのフォールバック検索用。
CREATE INDEX IF NOT EXISTS idx_invitations_email ON invitations (email);
CREATE INDEX IF NOT EXISTS idx_invitations_company_id ON invitations (company_id);

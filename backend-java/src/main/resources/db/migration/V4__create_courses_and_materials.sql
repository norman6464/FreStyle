-- courses / teaching_materials: 学習コンテンツ。Course / TeachingMaterial エンティティに対応。
-- 型は H2(PostgreSQL モード) と PostgreSQL の双方で通る書き方。本番は既存(GORM 製)のため no-op。
CREATE TABLE IF NOT EXISTS courses (
    id                 BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    company_id         BIGINT       NOT NULL,
    created_by_user_id BIGINT       NOT NULL,
    title              VARCHAR(255),
    description        VARCHAR,
    sort_order         INT          NOT NULL DEFAULT 100,
    is_published       BOOLEAN      NOT NULL DEFAULT FALSE,
    created_at         TIMESTAMP,
    updated_at         TIMESTAMP
);
CREATE INDEX IF NOT EXISTS idx_courses_company_id ON courses (company_id);

CREATE TABLE IF NOT EXISTS teaching_materials (
    id                 BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    company_id         BIGINT       NOT NULL,
    course_id          BIGINT,
    created_by_user_id BIGINT       NOT NULL,
    title              VARCHAR(255),
    content            VARCHAR,
    order_in_course    INT          NOT NULL DEFAULT 100,
    is_published       BOOLEAN      NOT NULL DEFAULT FALSE,
    created_at         TIMESTAMP,
    updated_at         TIMESTAMP
);
CREATE INDEX IF NOT EXISTS idx_teaching_materials_company_id ON teaching_materials (company_id);
CREATE INDEX IF NOT EXISTS idx_teaching_materials_course_id ON teaching_materials (course_id);

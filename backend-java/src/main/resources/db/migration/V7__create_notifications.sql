-- notifications: ユーザー向けの通知。Notification エンティティに対応する。
-- 型は H2(PostgreSQL モード) と PostgreSQL の双方で通る書き方にしている。
-- 既存(GORM AutoMigrate 由来)の本番テーブルと衝突しないよう IF NOT EXISTS にする。
CREATE TABLE IF NOT EXISTS notifications (
    id         BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    -- 通知は必ず宛先ユーザーに紐づくため NOT NULL。
    user_id    BIGINT       NOT NULL,
    type       VARCHAR(255) NOT NULL DEFAULT '',
    title      VARCHAR(255) NOT NULL DEFAULT '',
    body       VARCHAR      NOT NULL DEFAULT '',
    is_read    BOOLEAN      NOT NULL DEFAULT FALSE,
    -- 一覧の「作成日降順」契約を固定するため、未設定でも NULL が混ざらないよう DB 側で既定値を持つ。
    created_at TIMESTAMP    NOT NULL DEFAULT CURRENT_TIMESTAMP
);

-- 一覧・未読数はユーザー単位で引くため user_id に索引を張る。
CREATE INDEX IF NOT EXISTS idx_notifications_user_id ON notifications (user_id);

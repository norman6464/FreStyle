-- learning_reports: ユーザーの学習レポート(週次・月次集計を非同期生成)。LearningReport に対応。
-- 型は H2(PostgreSQL モード) と PostgreSQL の双方で通る書き方。既存(GORM)テーブルと衝突しないよう
-- IF NOT EXISTS にする。
CREATE TABLE IF NOT EXISTS learning_reports (
    id          BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    -- レポートは必ずユーザーに紐づくため NOT NULL。
    user_id     BIGINT       NOT NULL,
    period_from TIMESTAMP    NOT NULL,
    period_to   TIMESTAMP    NOT NULL,
    -- 生成ジョブの状態(pending / ready / failed)。生成要求時は pending で始まる。
    status      VARCHAR(255) NOT NULL DEFAULT 'pending',
    -- 生成完了後の成果物の S3 キー(未完了の間は NULL)。
    s3_key      VARCHAR,
    created_at  TIMESTAMP    NOT NULL DEFAULT CURRENT_TIMESTAMP
);

-- 一覧はユーザー単位で引くため user_id に索引を張る。
CREATE INDEX IF NOT EXISTS idx_learning_reports_user_id ON learning_reports (user_id);
